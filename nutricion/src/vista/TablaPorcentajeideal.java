private void cargartabla(){

        Connection con = (Connection) conexion.conectar();
        DefaultTableModel model = new DefaultTableModel();
        String sql = "Select estatura,edaddow,edadup,sexo,peso from pesos";
        Statement st;

        try {
            st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            this.jTable_pesos= new JTable(model);
            jScrollPane1.setViewportView(jTable_pesos);

            model.addColumn("Estatura");
            model.addColumn("De");
            model.addColumn("Hasta");
            model.addColumn("Sexo");
            model.addColumn("Peso");

            while (rs.next()) {
                Object fila[] = new Object[5];

                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                }

                model.addRow(fila);

            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al llenar grilla + e");
        }

}




